package javaStudy;

// *
// **
// ***
// ****
// *****

public class Study21 {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {      // i가 0일때 0, i가 4일때 4
            for (int j = 0; j <= i; j++) { // i가 0일때 0, j가 0일때 *
                System.out.print("*");
            }
            System.out.println();
            // i가 0일때 *
            // i가 1일때 **
            // i가 2일때 ***
            // i가 3일때 ****
            // i가 4일때 *****
            // 총 5줄 출력
            // i가 0부터 4까지 증가하면서 5번 반복
            // j는 0부터 i까지 증가하면서 i+1번 반복
            // 따라서 첫 번째 줄에는 1개, 두 번째 줄에는 2개, ..., 다섯 번째 줄에는 5개의 '*'이 출력됨
        }
//           i    j    k
//        *  0    4    0
//       **  1    3    0,1
//      ***  2    2    0,1,2
//     ****  3    1    0,1,2,3
//    *****  4    0    0,1,2,3,4
        for (int i = 0; i < 5; i++) {
            for (int j = 4-i; j >= 0; j--) {
                System.out.print(" ");
            }
            for (int k = i + 1; k > 0; k--) {
                System.out.print("*");
            }
            System.out.println();
        }
        // i가 0일때     *
        // i가 1일때    **
        // i가 2일때   ***
        // i가 3일때  ****
        // i가 4일때 *****
        // 총 5줄 출력
        // i가 0부터 4까지 증가하면서 5번 반복
        // j는 4부터 i까지 감소하면서 (4-i)번 반복하여 공백 출력
        // k는 0부터 i까지 증가하면서 (i+1)번 반복하여 '*' 출력
        // 따라서 첫 번째 줄에는 4개의 공백과 1개의 '*', 두 번째 줄에는 3개의 공백과 2개의 '*', ..., 다섯 번째 줄에는 0개의 공백과 5개의 '*'이 출력됨
        // 즉, 오른쪽 정렬된 삼각형이 만들어짐
        // 공백의 개수와 '*'의 개수가 서로 보완적인 관계에 있음
        // 각 줄에서 공백의 개수는 (총 줄 수 - 현재 줄 번호 - 1)이고, '*'의 개수는 (현재 줄 번호 + 1)임
        // 이를 통해 오른쪽 정렬된 삼각형 패턴이 형성됨
        // 즉, 각 줄에서 공백과 '*'의 개수를 조절하여 원하는 패턴을 만들 수 있음
    }
}
